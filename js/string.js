/*  STRING */

let myString = "jeena stanly";
let mySub = "Mathematics";

/* Length Property */
console.log(myString.length);
console.log(mySub.length);

/* How to fetch characters from a string */
console.log(myString.charAt(3));
console.log(myString.charAt(6));

/* How to find the first index of a word in astring */
console.log(myString.indexOf("sta"));
console.log(myString.indexOf("a"));
console.log(myString.indexOf("je"));
console.log(mySub.indexOf("at"));

/* How to find the last index of a letter or a word in a string  */

console.log(myString.lastIndexOf("a"));
console.log(myString.lastIndexOf("n"));
console.log(myString.lastIndexOf("sta"));
console.log(mySub.lastIndexOf("at"));

/* How to slice a word from a string */

console.log(myString.slice(0, 3));
console.log(mySub.slice(5, 11));
console.log(mySub.s)

/* How to split a string */

console.log(myString.split(" "));
console.log(mySub.split("e"));
console.log("hello,i am jeena".split(","));


/* How to change to lowercase and uppercase */

console.log(myString.toUpperCase());
console.log(myString.toLowerCase());

/* How to find if a string includes a word or a character */

console.log(myString.includes("st"));
console.log(myString.includes("en"));
console.log(myString.includes("oop"));
console.log(mySub.includes("mat"));




